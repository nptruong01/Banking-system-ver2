    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String txtSearch=this.txtSearch.getText();
        String colName=(String) this.cboSearch.getSelectedItem();
        Vector row = null;
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("Mã Chi Nhánh");
        model.addColumn("Vị Trí");
        model.addColumn("Tên Chi Nhánh");
        Branch lp;
        for (int i = 0; i < lpd.find(txtSearch, colName).size(); i++) {
            lp=lpd.find(txtSearch, colName).get(i);
            row=new Vector();
            row.add(lp.getBranch_id());
            row.add(lp.getLocation());
            row.add(lp.getBranch_name());
            
            
            model.addRow(row);
        }
        table.setModel(model);
        
        
    }//GEN-LAST:event_btnSearchActionPerformed


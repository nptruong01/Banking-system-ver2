    public void addTable()
    {
        Vector row = null;
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("Mã Chi Nhánh");
        model.addColumn("Vị Trí");
        model.addColumn("Tên Chi Nhánh");
        Branch lp;
        for (int i = 0; i < lpd.getList().size(); i++) {
            lp=lpd.getList().get(i);
            row=new Vector();
            row.add(lp.getBranch_id());
            row.add(lp.getLocation());
            row.add(lp.getBranch_name());
            
//            ps.setString(3, t.getBranch_id());
//             ps.setString(1, t.getLocation());
//             ps.setString(2, t.getBranch_name());
            model.addRow(row);
        }
        table.setModel(model);
        
    }
    
